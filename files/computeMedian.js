function computeMedian(array) {
    let arraySort = array.sort((a, b) => a - b)
    if (arraySort.length % 2 !== 0) {
        return arraySort [(arraySort.length - 1) / 2 ]
    } else {
        return (arraySort [arraySort.length / 2] + arraySort[arraySort.length / 2 - 1]) / 2
    }
}

